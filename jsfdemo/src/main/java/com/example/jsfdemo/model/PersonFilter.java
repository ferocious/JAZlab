package com.example.jsfdemo.model;

import java.util.Date;

public class PersonFilter {

	private String firstName;
	
	private String lastName;
	
	private String pesel;
	
	private Date birthdateFrom;
	
	private Date birthdateTo;
	
	private Float weightFrom;
	
	private Float weightTo;
	
	private Float heightFrom;
	
	private Float heightTo;
	
	private Date createdFrom;
	
	private Date createdTo;

	public String getFirstName() {
		return firstName;
	}

	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}

	public String getLastName() {
		return lastName;
	}

	public void setLastName(String lastName) {
		this.lastName = lastName;
	}

	public String getPesel() {
		return pesel;
	}

	public void setPesel(String pesel) {
		this.pesel = pesel;
	}

	public Date getBirthdateFrom() {
		return birthdateFrom;
	}

	public void setBirthdateFrom(Date birthdateFrom) {
		this.birthdateFrom = birthdateFrom;
	}

	public Date getBirthdateTo() {
		return birthdateTo;
	}

	public void setBirthdateTo(Date birthdateTo) {
		this.birthdateTo = birthdateTo;
	}

	public Float getWeightFrom() {
		return weightFrom;
	}

	public void setWeightFrom(Float weightFrom) {
		this.weightFrom = weightFrom;
	}

	public Float getWeightTo() {
		return weightTo;
	}

	public void setWeightTo(Float weightTo) {
		this.weightTo = weightTo;
	}

	public Float getHeightFrom() {
		return heightFrom;
	}

	public void setHeightFrom(Float heightFrom) {
		this.heightFrom = heightFrom;
	}

	public Float getHeightTo() {
		return heightTo;
	}

	public void setHeightTo(Float heightTo) {
		this.heightTo = heightTo;
	}

	public Date getCreatedFrom() {
		return createdFrom;
	}

	public void setCreatedFrom(Date createdFrom) {
		this.createdFrom = createdFrom;
	}

	public Date getCreatedTo() {
		return createdTo;
	}

	public void setCreatedTo(Date createdTo) {
		this.createdTo = createdTo;
	}
	
	
}
